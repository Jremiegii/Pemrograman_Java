package latihan3;

public class MenghitungNilaiRata_Rata {
    
    public static void main(String[] args){
        
        double Tugas1 = 7;
        double Tugas2 = 8;
        double Tugas3 = 9;
        double Rata_Rata;
        
        System.out.println("Tugas 1 = " + Tugas1);
        System.out.println("Tugas 2 = " + Tugas2);
        System.out.println("Tugas 3 = " + Tugas3);
        
        Rata_Rata = (Tugas1 + Tugas2 + Tugas3)/3;
        
        System.out.println("Nilai Rata-Rata adalah = " + Rata_Rata);
        
    }
    
}
