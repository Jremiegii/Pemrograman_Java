package latihan3;

public class WarungMakan {

    
    public static void main(String[] args) {
        
        int Makan = 8000;
        int Lauk = 3000;
        int Minum = 2000;
        
        double Total = Makan + Lauk + Lauk + Minum;
        
        System.out.println("Jumlah Makanan = " + Makan);
        System.out.println("Jumlah Lauk = " + Lauk);
        System.out.println("Jumlah Minuman = " + Minum);
        System.out.println("Total Harga =  " + Total);      
    }
    
}
