package Latihan6;

public class Latihan3 {
    
    public static void main(String[] args) {
        
        int sum = 0;
        int count;
        
        for (count = 0; count <= 7; count++){
            sum = sum + count;
        }
        System.out.println("Sigma Deret 1 Sampai Dengan 7 = " + sum);
        System.out.println("Nilai Sum = " + sum);
        System.out.println("Nilai Count = " + count);
    }
}
