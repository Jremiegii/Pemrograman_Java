package Latihan6;

import java.util.Scanner;

public class Latihan1 {

    public static void main(String[] args) {
        
        Scanner in = new Scanner(System.in);
        
        int Jumlah;
        
        System.out.print("Masukan Jumlah = ");
        Jumlah = in.nextInt();
        
        while(Jumlah > 0 ) {
            System.out.println(Jumlah + " Fransiskus Jremiegi Saputra");
            Jumlah--;
        }  
    }  
}
